const StatusChip: React.FC<StatusChipProps> = ({
  variant = 'default',
  label,
  emoji,
  icon,
  onClick,
  isFilter = false,
  active = false,
  size = 'md',
  className = ''
}) => {
  // Use custom icon if provided, otherwise use variant icon
  const displayIcon = icon ?? VariantIcons[variant]
  
  const sizeClasses = {
    sm: { padding: '4px 10px', fontSize: '0.7rem' },
    md: { padding: '6px 14px', fontSize: '0.8rem' },
    lg: { padding: '8px 18px', fontSize: '0.9rem' }
  }

  const baseClass = `ms-chip ms-chip-${variant}`
  const filterClass = isFilter ? 'ms-chip-filter' : ''
  const activeClass = active ? 'active' : ''

  return (
    <span
      className={`${baseClass} ${filterClass} ${activeClass} ${className}`}
      style={{ ...sizeClasses[size], display: 'inline-flex', alignItems: 'center', gap: '4px' }}
      onClick={onClick}
      role={onClick ? 'button' : undefined}
      tabIndex={onClick ? 0 : undefined}
      onKeyDown={onClick ? (e) => {
        if (e.key === 'Enter' || e.key === ' ') {
          e.preventDefault()
          onClick()
        }
      } : undefined}
    >
      {displayIcon && <span className="ms-chip-icon" style={{ display: 'flex', alignItems: 'center' }}>{displayIcon}</span>}
      {label}
    </span>
  )
}

export default StatusChip

